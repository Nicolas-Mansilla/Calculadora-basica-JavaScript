let num1 = prompt("Ingrese numero 1 para operar")
let num2 = prompt("Ingrese numero 2 para operar")


let suma = parseInt(num1) + parseInt(num2)
document.write("suma=", suma);
let resta = parseInt(num1) - parseInt(num2)
document.write("resta=", resta);
let div = parseInt(num1) / parseInt(num2)
document.write("division=", div);
let multiplicacion = parseInt(num1) * parseInt(num2)
document.write("multiplicacion=", multiplicacion);